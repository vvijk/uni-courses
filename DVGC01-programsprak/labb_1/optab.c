/**********************************************************************/
/* OBJECT ATTRIBUTES FOR THIS OBJECT (C MODULE)                       */
/**********************************************************************/
#define NENTS 4

static int optab[][NENTS] = {
    {'+', integer, integer, integer},
    {'+', real, real, real},
    {'+', integer, real, real},
    {'+', real, integer, real},
    {'*', integer, integer, integer},
    {'*', real, real, real},
    {'*', integer, real, real},
    {'*', real, integer, real},
    {'$', undef, undef, undef}};

/**********************************************************************/
/*  PRIVATE METHODS for this OBJECT  (using "static" in C)            */
/**********************************************************************/

/**********************************************************************/
/*  PUBLIC METHODS for this OBJECT  (EXPORTED)                        */
/**********************************************************************/
/**********************************************************************/
/* display the op tab                                                 */
/**********************************************************************/
void p_optab() {
    printf("\n________________________________________________________");
    printf("\nTHE OPERATOR TABLE");
    printf("\n________________________________________________________");
    printf("\n%10s %10s %10s %10s", "OPERATOR", "arg1", "arg2", "RESULT");
    printf("\n________________________________________________________");
    int i = 0;
    while (optab[i][0] != '$') {
        printf("\n%10s, %10s, %10s, %10s\n",
            tok2lex(optab[i][0]), tok2lex(optab[i][1]),
            tok2lex(optab[i][2]), tok2lex(optab[i][3]));
        i++;
    }
}

/**********************************************************************/
/* return the type of a binary expression op arg1 arg2                */
/**********************************************************************/
int get_otype(int op, int arg1, int arg2) {
    int i = 0;
    while (optab[i][0] != '$') {
        if (op == optab[i][0] && arg1 == optab[i][1] && arg2 == optab[i][2]) {
            return optab[i][3];
        }
        i++;
    }
    return optab[i][3];
}

